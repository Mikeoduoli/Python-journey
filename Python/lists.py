# We use lists when we want to
# represent a list of objects or numbers

names = ["John", "Doe", "You", "I"]
print(names)

# to access individual elements
# in a list use square bracket

print(names[2])

# Negative index in lists implies the last element
print(names[-1])

# Changing list element
names[0] = "Jon"
print(names)


